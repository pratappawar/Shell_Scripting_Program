#! /bin/bash 

	rand=$(($RANDOM%10))
	if [ $rand -eq 1 ]
		then 
		echo $rand "ONE"
	fi

	if [ $rand -eq 2 ]
      then
      echo $rand " TWO"
   fi

	if [ $rand -eq 3 ]
      then
      echo $rand " THREE"
   fi

	if [ $rand -eq 4 ]
      then
      echo $rand " FOUR"
   fi

	if [ $rand -eq 5 ]
      then
      echo $rand " FIVE"
   fi

	if [ $rand -eq 6 ]
      then
      echo $rand " SIX"
   fi

	if [ $rand -eq 7 ]
      then
      echo $rand " SEVEN"
   fi

	if [ $rand -eq 8 ]
      then
      echo $rand " EIGHT"
   fi

	if [ $rand -eq 9 ]
      then
      echo $rand " NINE"
   fi

	if [ $rand -eq 10 ]
      then
      echo $rand " TEN"
   fi


